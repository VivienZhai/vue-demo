package com.inspur.viz.model;

public class Widget {
	private String id;
	private String name;
	private String params;
	private String vizType;
	private String groupId;
	private String dataViewId;
	private String dataViewName;
	private String description;
	private String isPublished;
	private String updatedAt;
	private String createdAt;
	private String ownerId;
	private String checkSum;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getParams() {
		return params;
	}

	public void setParams(String params) {
		this.params = params;
	}

	public String getVizType() {
		return vizType;
	}

	public void setVizType(String vizType) {
		this.vizType = vizType;
	}

	public String getGroupId() {
		return groupId;
	}

	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}

	public String getDataViewId() {
		return dataViewId;
	}

	public void setDataViewId(String dataViewId) {
		this.dataViewId = dataViewId;
	}

	public String getDataViewName() {
		return dataViewName;
	}

	public void setDataViewName(String dataViewName) {
		this.dataViewName = dataViewName;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getIsPublished() {
		return isPublished;
	}

	public void setIsPublished(String isPublished) {
		this.isPublished = isPublished;
	}

	public String getUpdatedAt() {
		return updatedAt;
	}

	public void setUpdatedAt(String updatedAt) {
		this.updatedAt = updatedAt;
	}

	public String getCreatedAt() {
		return createdAt;
	}

	public void setCreatedAt(String createdAt) {
		this.createdAt = createdAt;
	}

	public String getOwnerId() {
		return ownerId;
	}

	public void setOwnerId(String ownerId) {
		this.ownerId = ownerId;
	}

	public String getCheckSum() {
		return checkSum;
	}

	public void setCheckSum(String checkSum) {
		this.checkSum = checkSum;
	}
}
